func main() {
	godotenv.Load()
	database.Init()

	fmt.Println("🚀 开始更新系统Agent的System Prompts...")
	fmt.Println(strings.Repeat("=", 70))

	// 查询所有系统Agent
	var agents []model.User
	if err := database.DB.Where("role = ? AND is_system = ?", model.RoleAgent, true).Order("id").Find(&agents).Error; err != nil {
		log.Fatal("❌ 查询系统 Agent 失败:", err)
	}

	fmt.Printf("📋 找到 %d 个系统 Agent\n\n", len(agents))

	successCount := 0
	errorCount := 0

	for _, agent := range agents {
		systemPrompt, exists := systemAgentPrompts[agent.Name]
		if !exists {
			fmt.Printf("⚠️  跳过: %s (未找到预定义的System Prompt)\n", agent.Name)
			continue
		}

		// 更新数据库
		if err := database.DB.Model(&agent).Update("system_prompt", systemPrompt).Error; err != nil {
			fmt.Printf("❌ 失败: %s (ID=%d) - %v\n", agent.Name, agent.ID, err)
			errorCount++
		} else {
			// 验证更新后的内容
			var updatedAgent model.User
			database.DB.First(&updatedAgent, agent.ID)

			status := "✅"
			if updatedAgent.SystemPrompt != systemPrompt {
				status = "❌ 验证失败"
			}

			fmt.Printf("%s %s (ID=%d)\n", status, agent.Name, agent.ID)
			fmt.Printf("   System Prompt: %d 字符\n", len(updatedAgent.SystemPrompt))
			successCount++
		}
		fmt.Println()
	}

	fmt.Println(strings.Repeat("=", 70))
	fmt.Println("📊 更新完成统计:")
	fmt.Printf("  ✅ 成功更新: %d\n", successCount)
	fmt.Printf("  ❌ 更新失败: %d\n", errorCount)
	fmt.Println(strings.Repeat("=", 70))

	if successCount == 12 {
		fmt.Println("\n🎉 所有12个系统Agent的System Prompt更新成功！")
		fmt.Println("现在可以运行 go run verify_system_prompts.go 验证")
	} else {
		fmt.Printf("\n⚠️  部分更新成功: %d/12\n", successCount)
	}
}
